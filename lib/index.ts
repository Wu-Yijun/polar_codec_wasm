// PolarCodec
// ---------------------------------------------------------------------------

/**
 * High-level wrapper around the Rust-backed {@link Codec}.
 *
 * `PolarCodec` manages Wasm initialization and provides a convenient
 * constructor that translates human-readable option names into the
 * low-level enum values expected by the Wasm layer.
 *
 * @typeParam K - Number of information bits (set at construction time).
 * @typeParam N - Codeword length (must be a power of two, >= K).
 *
 * @example
 * ```ts
 * // 5G-style frozen bits, no CRC, list-size L=4 (default)
 * const codec = new PolarCodec(96, 128);
 *
 * // With CRC-16 and GA frozen bits
 * const codec2 = new PolarCodec(96, 128, 4, {
 *   frozenBits: { type: "GA", sigma: 0.5 },
 *   crc: Crc.CRC_16_UMTS,
 * });
 * ```
 */
export class PolarCodec extends Codec {
  /** Number of information bits per codeword. */
  public readonly K: number;

  /** Total codeword length (information + frozen bits). */
  public readonly N: number;

  /** SCL list size used during decoding. */
  public readonly L: number;

  /** Frozen-bits construction method name. */
  public readonly METHOD: "5G" | "RM" | "GA";

  /** Resolved CRC parameters, or `undefined` when no CRC is used. */
  public readonly CRC: Required<DefinedCrc> | undefined;

  /**
   * Create a new Polar Codec instance.
   *
   * @param k  Number of information bits (must be <= n).
   * @param n  Codeword length, must be a power of two.
   * @param l  SCL list size (default `4`).  Larger values improve error
   *           correction at the cost of higher latency and memory.
   * @param options  Optional CRC and frozen-bits configuration.
   */
  constructor(k: number, n?: number, l: number = 4, options: Partial<PolarCodecOptions> = {}) {
    // Ensure the Wasm module is loaded before calling into Rust.
    initWasmSync();

    n ??= 1 << Math.ceil(Math.log2(k));
    let fb: FrozenBits;
    let sigma: number | null = null;
    let crc: Crc | null = null;
    let user_crc: Algorithm | null = null;
    let _crc: Required<DefinedCrc> | undefined = undefined;

    // ======================== Frozen Bits ========================
    // Determine which frozen-bits construction method to use.
    // Accepts a shorthand string or a detailed object form.
    if (typeof options.frozenBits === "string") {
      if (options.frozenBits === "5G") {
        fb = FrozenBits._5G;
      } else if (options.frozenBits === "RM") {
        fb = FrozenBits.RM;
      } else {
        throw new Error(`Invalid frozenBits string: ${options.frozenBits}`);
      }
    } else if (typeof options.frozenBits === "object" && options.frozenBits !== null) {
      if (options.frozenBits.type === "5G" || options.frozenBits.type === "RM") {
        fb = FrozenBits._5G;
      } else if (options.frozenBits.type === "GA") {
        fb = FrozenBits.GA;
        sigma = options.frozenBits.sigma ?? null;
      } else {
        throw new Error(`Invalid frozenBits object type: ${(options.frozenBits as any).type}`);
      }
    } else if (options.frozenBits === undefined) {
      // Auto-select: 5G for short blocks, RM for long blocks.
      fb = n <= 1024 ? FrozenBits._5G : FrozenBits.RM;
    } else {
      throw new Error(`Invalid frozenBits type: ${typeof options.frozenBits}`);
    }

    // ======================== CRC ========================
    // Determine CRC configuration.
    if (typeof options.crc === "number") {
      crc = options.crc;
      if (crc === Crc.UserDefined) {
        throw new Error("Use DefinedCrc object instead of the vague Crc.UserDefined id");
      }
    } else if (options.crc === undefined || options.crc === null) {
      crc = Crc.None;
    } else if (typeof options.crc === "object" && options.crc !== null) {
      // Build a custom CRC Algorithm from the user-supplied fields.
      const width = options.crc.width;
      const poly = BigInt(options.crc.poly);
      const init = options.crc.init !== undefined
        ? BigInt(options.crc.init)
        : ((1n << BigInt(width)) - 1n);          // default: all ones
      const refin = options.crc.refin ?? options.crc.refout ?? true;
      const refout = options.crc.refout ?? refin;
      const xorout = options.crc.xorout !== undefined
        ? BigInt(options.crc.xorout)
        : ((1n << BigInt(width)) - 1n);          // default: all ones

      user_crc = new Algorithm(width, poly, init, refin, refout, xorout);
      crc = Crc.UserDefined;
      _crc = { name: options.crc.name ?? "", width, poly, init, refin, refout, xorout };
    } else {
      throw new Error(`Invalid Crc type: ${typeof options.crc}`);
    }

    // Initialize the Rust-backed Codec through wasm-bindgen.
    super(k, n, l, crc, fb, user_crc, sigma);

    this.K = k;
    this.N = n;
    this.L = l;
    this.METHOD = fb === FrozenBits.GA ? "GA" : fb === FrozenBits.RM ? "RM" : "5G";
    this.CRC = _crc;
  }

  static bits_to_llr(bits: Uint8Array): Float32Array {
    return bits_to_llr(bits);
  }
}